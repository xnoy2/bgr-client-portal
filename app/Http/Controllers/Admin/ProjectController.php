    /**
     * POST /admin/projects/{ghlId}/documents
     */
    public function uploadDocument(Request $request, string $ghlId)
    {
        $project = Project::where('ghl_opportunity_id', $ghlId)->firstOrFail();

        $request->validate([
            'file'     => 'required|file|max:20480',
            'category' => 'nullable|in:contract,quote,invoice,plan,report,other',
        ]);

        $file     = $request->file('file');
        $filename = $file->getClientOriginalName();
        $title    = pathinfo($filename, PATHINFO_FILENAME);

        try {
            $path = $this->storage->upload($file, "documents/{$project->id}");
        } catch (\Throwable $e) {
            \Illuminate\Support\Facades\Log::error('Document upload failed', ['error' => $e->getMessage()]);
            return back()->withErrors(['file' => 'File upload failed: ' . $e->getMessage()]);
        }

        Document::create([
            'project_id'   => $project->id,
            'uploaded_by'  => auth()->id(),
            'title'        => $title,
            'filename'     => $filename,
            'storage_path' => $path,
            'storage_disk' => $this->storage->activeDisk(),
            'mime_type'    => $file->getMimeType(),
            'file_size'    => $file->getSize(),
            'category'     => $request->input('category', 'other'),
            'visibility'   => 'client',
            'sign_status'  => 'pending',
            'sent_at'      => now(),
        ]);

        // Notify the client that a document has been shared
        if ($project->client_id) {
            \App\Models\PortalNotification::notifyUser(
                userId:  $project->client_id,
                type:    'document_shared',
                title:   'New Document Shared',
                message: 'A new document "' . $filename . '" has been shared on your project.',
                url:     route('client.documents.index'),
            );
        }

        return back()->with('success', 'Document uploaded.');
    }

    /**
     * DELETE /admin/projects/{ghlId}/documents/{document}
