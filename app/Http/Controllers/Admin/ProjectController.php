    public function deleteDocument(string $ghlId, int $document)
    {
        $doc     = Document::findOrFail($document);
        $project = Project::where('ghl_opportunity_id', $ghlId)->firstOrFail();

        abort_unless($doc->project_id === $project->id, 403);

        if ($doc->storage_path) {
            $this->storage->delete($doc->storage_path, $doc->storage_disk ?? 'r2');
        }

        $doc->delete();

        return back()->with('success', 'Document deleted.');
    }

    /**
     * GET /admin/projects/{ghlId}/documents/{document}/download
     * Generates a short-lived presigned R2 URL and redirects the browser to it.
     * Uses raw int to bypass implicit route model binding which was causing 404s.
     */
    public function downloadDocument(string $ghlId, int $document)
    {
        $doc = Document::withTrashed()->findOrFail($document);

        // Re-fetch without trashed to confirm it's active
        if ($doc->trashed()) {
            abort(404, 'Document not found.');
        }

        if ($doc->storage_path) {
            // Local disk (dev environment) — stream directly
            if ($doc->storage_disk === 'local') {
                $content = \Illuminate\Support\Facades\Storage::disk('local')->get($doc->storage_path);
                return response($content ?? '', 200, [
                    'Content-Type'        => $doc->mime_type ?? 'application/octet-stream',
                    'Content-Disposition' => 'attachment; filename="' . str_replace('"', '', $doc->filename ?? 'document') . '"',
                    'Cache-Control'       => 'no-store',
                ]);
            }
            return $this->r2PresignedRedirect($doc->storage_path, $doc->filename ?? 'document');
        }

        // Legacy Cloudinary â€” proxy via HTTP
        if ($doc->url) {
            $response = \Illuminate\Support\Facades\Http::timeout(30)->get($doc->url);
            abort_unless($response->successful(), 502, 'Could not retrieve the file.');
            return response($response->body(), 200, [
                'Content-Type'        => $doc->mime_type ?? 'application/octet-stream',
                'Content-Disposition' => 'attachment; filename="' . str_replace('"', '', $doc->filename ?? 'document') . '"',
                'Cache-Control'       => 'no-store',
            ]);
        }

        // File was never stored (uploaded during transition period with missing storage config)
        return redirect()->back()->with('error', 'The file "' . $doc->filename . '" has no stored content. Please delete it and re-upload.');
    }

    private function r2PresignedRedirect(string $path, string $filename): \Illuminate\Http\RedirectResponse
    {
        $s3 = new \Aws\S3\S3Client([
            'version'                 => 'latest',
            'region'                  => 'auto',
            'endpoint'                => config('filesystems.disks.r2.endpoint'),
            'use_path_style_endpoint' => true,
            'credentials'             => [
                'key'    => config('filesystems.disks.r2.key'),
                'secret' => config('filesystems.disks.r2.secret'),
            ],
        ]);

        $cmd = $s3->getCommand('GetObject', [
            'Bucket'                        => config('filesystems.disks.r2.bucket'),
            'Key'                           => $path,
            'ResponseContentDisposition'    => 'attachment; filename="' . str_replace('"', '', $filename) . '"',
        ]);

        $url = (string) $s3->createPresignedRequest($cmd, '+10 minutes')->getUri();

        return redirect($url);
    }
